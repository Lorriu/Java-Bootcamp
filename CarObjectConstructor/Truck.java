package CarObjectConstructor;

public class Truck {

    String make;
    String model;
    int year;
    double price;

    //Truck class created with and arguemnet for how a truck should be structred
    Truck(String make, String model, int year, double price){

        this.make = make;
        this.model = model;
        this.year = year;
        this.price = price;
    }
    
}
